export const getLocationsByType = (type) => {
  return locations.filter(loc => loc.type === type);
};

export const getStateById = (id) => {
  return locations.find(loc => loc.id === id);
};

export const getCitiesByState = (stateName) => {
  const state = locations.find(loc => loc.name === stateName);
  return state ? [state.capital, ...state.majorCities] : [];
};

export const searchLocations = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return locations.filter(loc =>
    loc.name.toLowerCase().includes(lowercaseQuery) ||
    loc.capital.toLowerCase().includes(lowercaseQuery) ||
    loc.majorCities.some(city => city.toLowerCase().includes(lowercaseQuery))
  );
};
